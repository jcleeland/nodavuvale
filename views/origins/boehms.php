    <h2 class="font-bold text-xl leading-relaxed mb-6">
        Bavarian Roots
    </h2>
    <p class="text-lg leading-relaxed mb-6">
        <a href='?to=family/individual&individual_id=9'>Leonard Boehm</a> was born in Weissenhorn, Bayner in Bavaria somewhere between in 1826 and 1834. A US customs card 
        from November 11, 1853 states that a man by the name "Leonhard Boehm" arrived in New Orleans at the age of 20, having departed from Le Havre, France. 
        The card lists his place of birth as "Baviere, Germany", his nationality as "German", and his occupation as "farmer". 
        This card would suggest he was born in 1933.
    </p>
    <h2 class="font-bold text-xl leading-relaxed mb-6">
        Emigration to the US
    </h2>
    <p class="text-lg leading-relaxed mb-6">
        Leonard's parents were likely farmers, given his age when he arrived in New Orleans. The 1840s and 1850s were marked by agricultural failures in Europe (including
        the "Great European Famine" of 1846-1852) which may have prompted Leonard to seek a new life in the US. Germany also experienced a series of revolutions in 1848
        although Bavaria's king Ludwig I was able to suppress the revolutionaries. Many young men, especially liberals, intellectuals and those involved in political
        activism, left Germany for the US during this time.
    </p>
    <p class="text-lg leading-relaxed mb-6">
        There is some evidence to suggest that Leonard was Jewish, or at least of Jewish descent. The name "Boehm" is a common Jewish surname, and many descendants
        have traces of Sephardic Jewish DNA. It is possible that Leonard was fleeing persecution in Bavaria, which was known for its anti-semitism. Many Jews emigrated
        to the US during the 1840s and 1850s to escape persecution.
    </p>
    <h2 class="font-bold text-xl leading-relaxed mb-6">
        The South Pacific and the Cotton Boom
    </h2>
    <p class="text-lg leading-relaxed mb-6">
        At some point Leonard made the decision to leave the US and travel to the South Pacific. This likely occurred around the 1860s, during the American Civil War;
        a time when Fiji experienced a cotton boom - a crop that the northern US states could no longer access from the South.
    </p>
    <p class="text-lg leading-relaxed mb-6">
        Records show that Leonard purchased the island of Kanacea from Tui Cakau in 1865 which he used to grow Cotton, although he struggled to find labourers
        and had to bring in workers from other islands.
    </p>
    <p class="text-lg leading-relaxed mb-6">
        He built a thriving business, with three ships and he met many travellers including the brother of a chief from Nataleira on the main island. According the 
        keepers of history at Nataleira, Leonard was introduced to the Chief and found good favour with him. This relationship led him to be offered the hand
        of <a href='?to=family/individual&individual_id=1'>Losana Nataleira</a> - one of the sisters of the village chiefs at the time.
    </p>
    <p class="text-lg leading-relaxed mb-6">
        And this was the origin of our families ties to Fiji. Leonard and Soli (Losana) had 5 children, including <a href='?to=family/individual&individual_id=3'>Louisa</a>,
        <a href='?to=family/individual&individual_id=5'>Mathilda</a>, <a href='?to=family/individual&individual_id=4'>Wilhelm</a>, 
