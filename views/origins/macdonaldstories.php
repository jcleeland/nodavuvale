    <h1 class="text-4xl font-bold mb-6">Macdonald Clan Stories: Legends of Honor and Survival</h1>
    
    <h2 class="font-bold text-xl leading-relaxed mb-6">The Massacre of Glencoe (1692)</h2>
    <p class="text-lg leading-relaxed mb-6">
        <img src="images/glencoe_massacre.webp" alt="Glencoe Massacre" class="float-left h-64 image-cover w-auto mx-4 mb-4 rounded-lg shadow-lg">
        One of the most infamous events in Scottish history, the <a href="https://en.wikipedia.org/wiki/Massacre_of_Glencoe" target="_blank">Massacre of Glencoe</a>, remains a defining moment in Macdonald heritage. On February 13, 1692,
        government forces, under orders from King William III, betrayed the Macdonalds of Glencoe after being welcomed as guests. 
    </p>
    <p class="text-lg leading-relaxed mb-6">
        In late January 1692, approximately 120 men from the Earl of Argyll's Regiment of Foot arrived in Glencoe. Their commander was Robert Campbell of Glenlyon, 
        an impoverished local landowner whose niece was married to one of MacIain's sons. Campbell carried orders for 'free quarter', an established alternative to 
        paying taxes in what was a largely non-cash society. The Glencoe MacDonalds had themselves been similarly billeted on the Campbells when serving with the 
        Highland levies used to police Argyll in 1678.
    </p>
    <p class="text-lg leading-relaxed mb-6">
        The troops stayed in the MacDonalds' homes for nearly two weeks, enjoying the clan's hospitality. On the morning of February 13, 1692, the soldiers turned 
        on their hosts. Thirty-eight members of the clan were murdered, and many more died from exposure after fleeing into the winter mountains. The betrayal is 
        remembered as a symbol of resilience and unity among the Macdonalds.
    </p>
    
    <h2 class="font-bold text-xl leading-relaxed mb-6">The Feud with Clan MacLeod</h2>
    <p class="text-lg leading-relaxed mb-6">
        The Macdonalds and MacLeods were bitter rivals in the Highlands. One of their most infamous conflicts was the <a href="https://en.wikipedia.org/wiki/Battle_of_Coire_Na_Creiche" target="_blank">Battle of Coire na Creiche (1601)</a>,
        the last clan battle fought on the Isle of Skye. The Macdonalds emerged victorious, solidifying their dominance in the Hebrides.
    </p>
    
    <h2 class="font-bold text-xl leading-relaxed mb-6">The Massacre Cave of Eigg</h2>
    <p class="text-lg leading-relaxed mb-6">
        <img src="images/eigg_massacre_cave.jpg" class="float-right h-64 image-cover w-auto mx-4 mb-4 rounded-lg shadow-lg" alt="The entrance to the cave at Eigg">
        In a tragic episode of clan warfare, in the winter of 1577, a band of furious Macleods landed on the Isle of Eigg seeking revenge for the castration of one of their clansmen. 
        The story goes that the Macleod Clansman had been punished for making unwanted advances towards the ladies of the Island.
    </p>
    <p class="text-lg leading-relaxed mb-6">
        Somewhere between 200 and 400 Macdonalds sought refuge in St Francis' Cave to escape the MacLeods.
        However their hideaway was discovered by the Macleods, and due to its tiny entrance after a fire was set at the cave entrance, they were trapped inside suffocating all within. The story is a grim
        reminder of the brutal nature of Scottish clan feuds. Read more about the <a href="https://theculturetrip.com/europe/united-kingdom/scotland/articles/the-story-behind-scotlands-massacre-cave" target="_blank">Massacre Cave of Eigg</a>.
    </p>
    
    <h2 class="font-bold text-xl leading-relaxed mb-6">Cú Chulainn and Dunscaith Castle</h2>
    <p class="text-lg leading-relaxed mb-6">
        <a href="https://en.wikipedia.org/wiki/Dunscaith_Castle" title="Dunscaith Castle" target="_blank">Dunscaith Castle</a>, a historic Macdonald stronghold on the Isle of Skye, is linked to the legend of <a href="https://en.wikipedia.org/wiki/C%C3%BA_Chulainn" target="_blank">Cú Chulainn</a>, a great warrior from Irish mythology.
        It is said that he trained in the arts of war under the warrior woman Scáthach at this castle. The legend connects the Macdonalds to an ancient tradition of
        strength and battle prowess.
    </p>
    
    <h2 class="font-bold text-xl leading-relaxed mb-6">The Lords of the Isles</h2>
    <p class="text-lg leading-relaxed mb-6">
        The Macdonalds were once the rulers of the western seaboard of Scotland, holding the title of <a href="https://highcouncilofclandonald.com/the-kings-and-lords-of-the-isles/" target="_blank">Lords of the Isles</a>.
        This status granted them almost king-like authority over the Hebrides and parts of western Scotland, rivaling the Scottish Crown between the 13th century until the late 15th century.
    </p>
    
    <h2 class="font-bold text-xl leading-relaxed mb-6">The Battle of Harlaw (1411)</h2>
    <p class="text-lg leading-relaxed mb-6">
        The <a href="https://en.wikipedia.org/wiki/Battle_of_Harlaw" target="_blank">Battle of Harlaw</a> was a major confrontation between Highland and Lowland forces in Scotland.
        Donald, Lord of the Isles, led the Macdonald forces in an attempt to expand their domain into the north-east of Scotland. Though inconclusive, the battle was
        one of the bloodiest in Scottish history and marked a turning point for Macdonald influence in the region.
    </p>
